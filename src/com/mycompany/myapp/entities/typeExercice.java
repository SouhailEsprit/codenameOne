public class typeExercice {

    private int id;
    private String name;

    public typeExercice() {
    }

    public typeExercice(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "typeExercice{" + "id=" + id + ", name=" + name + "}";
    }

}
